CELLSIZE = HEIGHT / len(Map[0])


def renderMap2D(screen):
    b = 0
    for y, row in enumerate(Map):
        for x, col in enumerate(row):
            pos = createVector(x * CELLSIZE, y * CELLSIZE)
            pygame.draw.rect(screen, MapMaterials[col].colour,
                             pygame.Rect(pos.x + b, pos.y + b, CELLSIZE - b * 2, CELLSIZE - b * 2))


def MiniMap2D(screen, mmWidth, Player, offset):
    mmCellSize = mmWidth / len(Map[0])

    for y, row in enumerate(Map):
        for x, col in enumerate(row):
            pos = createVector(x * mmCellSize, y * mmCellSize)
            pygame.draw.rect(screen, MapMaterials[col].colour,
                             pygame.Rect(pos.x + offset[0], pos.y + offset[1], mmCellSize * 2, mmCellSize * 2))

    px, py = (Player.pos.x/HEIGHT)*mmWidth + offset[0], (Player.pos.y/HEIGHT)*mmWidth + offset[1]
    pygame.draw.line(screen, (255, 0, 0), (px, py), (mmWidth*0.05*math.cos(Player.facing)+px, mmWidth*0.05*math.sin(Player.facing)+py))
    pygame.draw.circle(screen, (0, 255, 0), (px, py), mmWidth*0.01)


def renderMap3D(screen, distanceSegmentData, maxDistance):
    if distanceSegmentData:
        pygame.draw.rect(screen, (135, 206, 235), pygame.Rect(0, 0, WIDTH, HEIGHT / 2))
        pygame.draw.rect(screen, (120, 120, 120), pygame.Rect(0, HEIGHT / 2, WIDTH, HEIGHT / 2))

        columnWidth = WIDTH / len(distanceSegmentData)

        for idx, Data in enumerate(distanceSegmentData):
            perc = 1 - (Data[0] / maxDistance)
            # columnHeight = 1200 * perc
            columnHeight = (CELLSIZE*2 * 1200) / Data[0] if Data[0] > 0 else 1200

            if columnHeight > 0 and Data[0] <= maxDistance:
                if Data[1] == 'V':
                    pygame.draw.rect(screen, (231*perc, 91*perc, 127*perc), pygame.Rect(idx * columnWidth, HEIGHT/2 - columnHeight/2, columnWidth+1, columnHeight))

                elif Data[1] == 'H':
                    pygame.draw.rect(screen, (245*perc, 181*perc, 187*perc), pygame.Rect(idx * columnWidth, HEIGHT/2 - columnHeight/2, columnWidth+1, columnHeight))

                else:
                    raise Exception("NO ORIENTATION")
